package kr.happyjob.study.vo.diary;

public class DiaryVO {
	private String loginId;
	private String dTitle;
	private String dWeather;
	private String dMood;
	private String dContents;
	private String dDiarydt;
	private String dRegdate;
	
	public String getLoginId() {
		return loginId;
	}
	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}
	public String getdTitle() {
		return dTitle;
	}
	public void setdTitle(String dTitle) {
		this.dTitle = dTitle;
	}
	public String getdWeather() {
		return dWeather;
	}
	public void setdWeather(String dWeather) {
		this.dWeather = dWeather;
	}
	public String getdMood() {
		return dMood;
	}
	public void setdMood(String dMood) {
		this.dMood = dMood;
	}
	public String getdContents() {
		return dContents;
	}
	public void setdContents(String dContents) {
		this.dContents = dContents;
	}
	public String getdDiarydt() {
		return dDiarydt;
	}
	public void setdDiarydt(String dDiarydt) {
		this.dDiarydt = dDiarydt;
	}
	public String getdRegdate() {
		return dRegdate;
	}
	public void setdRegdate(String dRegdate) {
		this.dRegdate = dRegdate;
	}
}
